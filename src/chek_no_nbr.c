#include "push_swap.h"

int	chek_no_nbr(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '+' || str[0] == '-')
		i++;
	if (!str[i])
		return (1);
	while (str && str[i])
	{
		if (!(str[i] <= '9' && str[i] >= '0'))
			return (1);
		i++;
	}
	return (0);
}
